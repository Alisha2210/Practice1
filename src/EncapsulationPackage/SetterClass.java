package EncapsulationPackage;

public class SetterClass {
    private int marks;
    private String name;

    public void setMarks(int marks) {
        this.marks = marks;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMarks() {
        return marks;
    }

    public String getName() {
        return name;
    }
}
